function buildMapUrls(name: string, address: string, kakaoUrl: string, naverUrl?: string) {
  const q = encodeURIComponent(`${name} ${address}`);
  return {
    // naverUrl을 안 넘기면 검색 결과 링크로 대체한다 — 검색 결과는 동명이인 매장이 여러 개
    // 뜨거나 엉뚱한 곳이 먼저 나올 수 있어 정확하지 않다. kakaoUrl처럼 각 장소의 실제
    // 상세 페이지 URL(예: https://map.naver.com/p/entry/place/12345678)을 넣어주면
    // 그쪽이 우선한다.
    mapUrlNaver: naverUrl ?? `https://map.naver.com/p/search/${q}`,
    mapUrlKakao: kakaoUrl,
  };
}

function place(
  p: Omit<Place, "mapUrlNaver" | "mapUrlKakao" | "photos"> & { kakaoUrl: string; naverUrl?: string },
): Place {
  const { kakaoUrl, naverUrl, ...rest } = p;
  return {
    ...rest,
    ...buildMapUrls(p.name, p.address, kakaoUrl, naverUrl),
    photos: PLACE_PHOTOS[p.name] ?? [],
  };
}

