/**
 * 홈 화면 랭킹 섹션용 임시 정렬 기준. 실제 조회수·저장수 집계가 아직 없어서,
 * 이미 갖고 있는 큐레이션 점수(대접감·대화가능성·서비스·접근성 평균)로 대체한다.
 * 트래픽 데이터가 쌓이면 이 함수만 실집계 기준으로 바꾸면 된다.
 */
export function getTopRatedPlaces(limit = 5): Place[] {
  // 대접감·서비스 점수는 가격이 높은 장소일수록 자연히 높게 나오는 경향이 있다.
  // 순수 품질 점수만으로 정렬하면 홈 화면 첫인상이 고가 오마카세 위주로 쏠려,
  // "청첩장 모임"이라는 다양한 예산대의 실제 사용 맥락과 멀어진다.
  // 1인 평균가에 완만한 페널티를 줘서 가격대가 고르게 섞여 보이도록 보정한다.
  const valueScore = (p: Place) => {
    const quality = (p.hospitalityScore + p.conversationScore + p.serviceScore + p.accessScore) / 4;
    const avgPrice = (p.priceMin + p.priceMax) / 2;
    return quality - avgPrice / 50000;
  };
  return [...PLACES].sort((a, b) => valueScore(b) - valueScore(a)).slice(0, limit);
}
